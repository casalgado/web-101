// para hacer logica se utilizan expresiones
// cada expresion se evalua a un resultado que es verdadero o falso.
// se puedes utilizar con strings, numeros, arrays o otro tipo de objetos.

// igualdad

"uno" == "uno"; // true
1 == 2; // false

"tres" !== "tres"; // false
4 !== 4; // true

// mayor o Menor

4 > 5; // false
10 < 15; // true

// mayor o igual, menor o igual

3 <= 2; // false
3 <= 3; // true
3 <= 4; // true

10 >= 9; // true
10 >= 10; // true
10 >= 11; // false
